# ── 생성 함수 ────────────────────────────────────────────────────

def generate_doc(category: str, name: str, prompt: str) -> dict:
    message = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=800,
        messages=[{"role": "user", "content": prompt}],
    )
    content = message.content[0].text.strip()
    return {
        "id": f"{category}_{name}",
        "category": category,
        "name": name,
        "content": content,
    }


def generate_group(category: str, prompts: dict[str, str], delay: float = 0.5) -> list[dict]:
    print(f"\n[{category}] 생성 시작 ({len(prompts)}개)")
    results = []
    items = list(prompts.items())
    for i, (name, prompt) in enumerate(items, 1):
        print(f"  {i}/{len(items)} {name} ...", end=" ", flush=True)
        try:
            doc = generate_doc(category, name, prompt)
            results.append(doc)
            print(f"완료 ({len(doc['content'])}자)")
        except Exception as e:
            print(f"실패: {e}")
        if i < len(items):
            time.sleep(delay)
    return results


# ── 실행 ─────────────────────────────────────────────────────────

def main():
    if sys.stdout.encoding != "utf-8":
        sys.stdout.reconfigure(encoding="utf-8", errors="replace")

    all_new_docs = []

    groups = [
        ("용신", PROMPTS["용신"]),
        ("격국", PROMPTS["격국"]),
        ("대운세운", PROMPTS["대운세운"]),
        ("합충", PROMPTS["합충"]),
    ]

    for category, prompts in groups:
        docs = generate_group(category, prompts)
        all_new_docs.extend(docs)

        out_path = OUTPUT_DIR / f"{category}.json"
        out_path.write_text(json.dumps(docs, ensure_ascii=False, indent=2), encoding="utf-8")
        print(f"  저장: {out_path}")

    # 기존 all.json과 병합
    all_path = OUTPUT_DIR / "all.json"
    existing = json.loads(all_path.read_text(encoding="utf-8"))
    existing_ids = {d["id"] for d in existing}

    added = [d for d in all_new_docs if d["id"] not in existing_ids]
    merged = existing + added

    all_path.write_text(json.dumps(merged, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"\n=== all.json 병합 완료: 기존 {len(existing)}개 + 신규 {len(added)}개 = 총 {len(merged)}개 ===")


if __name__ == "__main__":
    main()
