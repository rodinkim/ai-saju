# ── 저장 함수 ─────────────────────────────────────────────────────

def main():
    # 카테고리별 파일 저장
    by_category: dict[str, list] = {}
    for doc in DOCS:
        by_category.setdefault(doc["category"], []).append(doc)

    for cat, docs in by_category.items():
        out_path = OUTPUT_DIR / f"{cat}.json"
        out_path.write_text(json.dumps(docs, ensure_ascii=False, indent=2), encoding="utf-8")
        print(f"저장: {out_path.name} ({len(docs)}개)")

    # all.json 병합 (기존 id 교체)
    all_path = OUTPUT_DIR / "all.json"
    existing = json.loads(all_path.read_text(encoding="utf-8"))
    new_ids = {d["id"] for d in DOCS}

    # 기존에서 이번 id 제거 후 새 문서로 교체
    kept = [d for d in existing if d["id"] not in new_ids]
    merged = kept + DOCS

    all_path.write_text(json.dumps(merged, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"\n=== all.json 갱신 완료: {len(kept)}개 유지 + {len(DOCS)}개 교체 = 총 {len(merged)}개 ===")


if __name__ == "__main__":
    main()
